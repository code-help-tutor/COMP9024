<h2>1. For stages 2 &amp; 3, can I change my solution to stage 1</tt>?</h2>

  <p> Yes. You can change your implementations directly. The stage 1 tests for marking will only use input cases for which stages 2 and 3 give the same result.</p>

   <i>Note:</i> This means that you could begin direcly with stage 3. However, it is good and established practice to develop complex software in well-defined stages, each of which is thoroughly tested before moving to the next.</p>

